# if..else
# syntax
'''if expression:
    statement($)
else:
    statement($)
'''
a_value = int(input("Enter a Number value : "))
if a_value == 100:
    print("Condition is true")
    print(a_value)
else:
    print("Condition is false")
    print(a_value)

b_value = int(input("Enter a Number value : "))
if b_value == 100:
    message = "Condition is True"
else:
    message = "Condition is False"
print(message)